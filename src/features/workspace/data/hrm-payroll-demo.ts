export function formatPayrollAmount(amount: number, currency: string) {
  const locales: Record<string, string> = {
    INR: "en-IN",
    GBP: "en-GB",
    EUR: "de-DE",
    SGD: "en-SG",
    AED: "en-AE",
  };
  return new Intl.NumberFormat(locales[currency] ?? "en-US", {
    style: "currency",
    currency,
    maximumFractionDigits: 0,
  }).format(amount);
}
