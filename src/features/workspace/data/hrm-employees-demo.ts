export function getHrmEmployee(
  employees: HrmEmployeeRecord[],
  id: string,
): HrmEmployeeRecord | undefined {
  return employees.find((e) => e.id === id);
}

export function getManagerName(
  employees: HrmEmployeeRecord[],
  managerId: string | null,
): string {
  if (!managerId) return "—";
  return getHrmEmployee(employees, managerId)?.name ?? "—";
}

export function countDirectReports(
  employees: HrmEmployeeRecord[],
  managerId: string,
): number {
  return employees.filter((e) => e.managerId === managerId).length;
}
